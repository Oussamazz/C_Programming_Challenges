int	ft_atoi(const char *str) {
	long n = 0, i = 0, sig = 1;

	while (str[i] == ' ' || str[i] == '\t')
		i++;

	if (str[i] == '-' || str[i] == '+') {
		if (str[i] == '-')
			sig = -1;
		i++;
	}

	while (str[i] > 47 && str[i] < 58) {
		n = n * 10 + (str[i] - 48);
		i++;
	}
	return (n * sig);
}
